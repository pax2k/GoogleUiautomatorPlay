#!/bin/bash
echo "**************************** Starting Build of APK ****************************"
ant clean debug uninstall install

echo "*******************************************************************************"
echo "************************* Main application deployed ***************************"
echo "************************* Build UI test project *******************************"
echo "*******************************************************************************"
cd UiTestsTest/
ant build

echo "*******************************************************************************"
echo "************************* Uploading test project ******************************"
echo "*******************************************************************************"
adb push bin/UiTest.jar /data/local/tmp

echo "*******************************************************************************"
echo "************************* Starting UI test ************************************"
echo "*******************************************************************************"
adb shell uiautomator runtest UiTest.jar -c no.koteng.awesomeapp.test.TestUi



